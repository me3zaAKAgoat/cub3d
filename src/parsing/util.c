#include "cub3d.h"

void	print_map(t_map_element **map)
{
	size_t  y;
	size_t  x;

	y = 0;
	while (map[y])
	{
		x = 0;
		while (map[y][x] != HORIZONTAL_TERM)
		{
			printf("%d ", map[y][x]);
			x++;
		}
		puts("");
		y++;
	}
}